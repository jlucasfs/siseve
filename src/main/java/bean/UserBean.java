package bean;

public class UserBean {

	private long id;

	private String nome;

	public long getId() {
		return this.id;
	}

	public void setId( long idParam ) {
		this.id = idParam;
	}

	public String getNome() {
		return this.nome;
	}

	public void setNome( String nomeParam ) {
		this.nome = nomeParam;
	}
}
